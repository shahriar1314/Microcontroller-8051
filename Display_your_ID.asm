		MOV SP, #70H
		MOV PSW, #00H
		RS EQU P2.0
		RW EQU P2.1
		ENBL EQU P2.2

		MOV A,#38H
		LCALL COMMAND
		LCALL DELAY
		MOV A,#00EH
		LCALL COMMAND
		LCALL DELAY
		MOV A,#01
		LCALL COMMAND
		LCALL DELAY
		MOV A, #06H
		LCALL COMMAND
		LCALL DELAY
		MOV A,#80H
		LCALL COMMAND
		LCALL DELAY
		ACALL DELAY
		MOV DPTR,#MSG


LOOP:		CLR A
		MOVC A,@A+DPTR
		JZ FINISH
		LCALL DISPLAY
		LCALL DELAY
		INC DPTR
		LJMP LOOP

FINISH:		SJMP $		
		
COMMAND:	LCALL READY
		MOV P3,A
		CLR RS
		CLR RW
		SETB ENBL
		LCALL DELAY
		CLR ENBL
		RET
		
DISPLAY: 	LCALL READY
		MOV P3,A
		SETB RS
		CLR RW
		SETB ENBL
		LCALL DELAY
		CLR ENBL
		RET
		
READY:  	SETB P3.7
		CLR RS
		SETB RW
WAIT:		CLR ENBL
		ACALL DELAY
		SETB ENBL
		JB P3.7,WAIT
		RET
		
DELAY:  	MOV R3,#50
AGAIN_2:	MOV R4,#255
AGAIN:  	DJNZ R4,AGAIN
        	DJNZ R3,AGAIN_2
        	RET
