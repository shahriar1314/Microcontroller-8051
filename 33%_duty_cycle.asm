ORG 00H 
MOV P1, #0H		;SETTING PORT 1 AS OUTPUT PORT

AGAIN:	SETB P1.7	;GIVING HIGH PULSE
	ACALL DELAY 	;DELAY FOR 5ms
	CLR P1.7 	;GIVING LOW PULSE
	ACALL DELAY 	;DELAY FOR 5ms
	ACALL DELAY 	;DELAY FOR 5ms
	SJMP AGAIN 	;KEEP DOING THIS 

ORG 300H 
DELAY:	MOV R2, #18
L2:	MOV R1, #255
L1: 	DJNZ R1, L1	;AS CALCULATED, THE LOOP WILL RUN FOR 18*255=4587 TIMES
	DJNZ R2, L2

	RET

END
	


